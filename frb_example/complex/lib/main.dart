  // This widget is the root of your application.
  @override
  Widget build(BuildContext context) {
    return const MaterialApp(
      title: 'Flutter Rust Bridge Example',
      home: MyHomePage(),
    );
  }
}

class MyHomePage extends StatefulWidget {
  const MyHomePage({Key? key}) : super(key: key);

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  late final dylib =
      Platform.isAndroid ? DynamicLibrary.open('libflutter_rust_bridge_example.so') : DynamicLibrary.process();
  late final api = ExampleApi(ExampleWire(dylib));

  Uint8List? exampleImage;
  String? exampleText;

  @override
  void initState() {
    super.initState();
    _callExampleFfi();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text('Flutter Rust Bridge Example'),
      ),
      body: ListView(
        children: [
          _buildCardUi(
            'Example 1',
            'Image generated by Rust and displayed by Flutter/Dart',
            exampleImage != null ? SizedBox(width: 200, height: 200, child: Image.memory(exampleImage!)) : Container(),
          ),
          _buildCardUi(
            'Example 2',
            'Complex struct/class is auto passed through FFI and here is the result',
            Text('Output from Rust: ' + (exampleText ?? '')),
          ),
        ],
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: () => _callExampleFfi(),
        child: const Icon(Icons.refresh),
      ), // This trailing comma makes auto-formatting nicer for build methods.
    );
  }

  Widget _buildCardUi(String title, String subtitle, Widget child) {
    return Container(
      padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 8),
      child: Card(
        child: Container(
          padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 8),
          child: Column(
            children: [
              Text(title, style: TextStyle(fontSize: 18, fontWeight: FontWeight.bold)),
              Container(height: 8),
              Text(subtitle),
              child,
            ],
          ),
        ),
      ),
    );
  }

  Future<void> _callExampleFfi() async {
    final randomValue = 1.0 + Random().nextDouble();

    final receivedText = await api.treeTraversal(root: _createExampleTree());
    setState(() => exampleText = receivedText);

    final zoomPoint = Point(
        x: 0.3602404434376143632361252444495453084826078079585857504883758147401953460592181003117529367227734263962337317297249877373200353726832853176645324012185215795,
        y: -0.6413130610648031748603750151793020665794949522823052595561775430644485741727536902556370230689681162370740565537072149790106973211105273740851993394803287437606238596262);

    final receivedImage = await api.drawMandelbrot(
        imageSize: Size(width: 200, height: 200), rightBottom: Point(x: randomValue, y: randomValue), numThreads: 4);
    setState(() => exampleImage = receivedImage);
  }

  TreeNode _createExampleTree() => TreeNode(name: 'root', children: [
        for (var i = 0; i < 1 + Random().nextInt(2); ++i)
          TreeNode(name: 'child_$i', children: [
            for (var j = 0; j < 1 + Random().nextInt(2); ++j) TreeNode(name: 'grandchild_$j', children: [])
          ]),
      ]);
}
